import { DataTypes } from "sequelize";
import dbConnection from "../config/dbConfig.js";

const ApiEndpoint = dbConnection.define(
  "api_endpoint",
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      autoIncrement: true,
      primaryKey: true,
    },
    name: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    type: {
      type: DataTypes.ENUM("backend", "frontend"),
      allowNull: false,
      defaultValue: "backend",
    },
    status: {
      type: DataTypes.ENUM("active", "inactive"),
      allowNull: false,
      defaultValue: "active",
    },
    created_at: {
      type: DataTypes.DATE,
      defaultValue: DataTypes.NOW,
    },
    updated_at: {
      type: DataTypes.DATE,
      defaultValue: DataTypes.NOW,
    },
  },
  { timestamps: false, tableName: "api_endpoint" }
);

export default ApiEndpoint;
